
                ifndef _CHARACTER_PATH_DISTANCE_
                define _CHARACTER_PATH_DISTANCE_
; -----------------------------------------
; рассчитать знаковое смещение от текущей позиции объекта до точки пути
; In:
;   DE - адрес структуры FPath
;   IX - адрес структуры объекта (FObjectCharacter)
; Out:
;   HL - знаковое смещение по вертикали, в четвертях пикселя
;   DE - знаковое смещение по горизонтали, в четвертях пикселя
; Corrupt:
;   HL, DE, AF
; Note:
;   значения HL и DE являются знаковыми 16-битными целыми в четвертях пикселя:
;      1 единица = 1/4 пикселя, 4 единицы = 1 пиксель;
;   текущая позиция учитывает локальное смещение объекта внутри гекса;
;   точкой назначения пока является центр гекса, указанного в FPath.HexCoord;
;   FPath.HexCoord.X не должен находиться по адресу #xxFF, т.к. переход к Y выполняется через INC E;
;   код расположен в странице 0
; -----------------------------------------
Distance:       PUSH BC                                                         ; сохранить исходное значение BC

                ; ToDo: добавить движение к произвольной точке внутри целевого гекса;
                ;       локальные TargetX и TargetY получать через FPath.WayPointIdx либо из координат заданной точки;
                ;       для этого в текущих формулах достаточно заменить координаты центра на локальные координаты точки;
                ;       преобразование в глобальные координаты и последующий расчёт DDA остаются без изменений;
                ;       пока вместо локальных координат точки используются координаты центра гекса

                ; -----------------------------------------
                ; преобразование текущей горизонтальной позиции объекта
                ; в глобальную координату, выраженную в четвертях пикселя:
                ;   HexStrideX = HEXTILE_SIZE_X * 8 * 4 = HEXTILE_SIZE_X << 5 = 192;
                ;   HalfHexX   = HexStrideX / 2        = HEXTILE_SIZE_X << 4 = 96;
                ;   CurrentX   = HexX * HexStrideX + LocalX - (1 - (HexY & 1)) * HalfHexX
                ; -----------------------------------------
                ; расширить координату гекса по горизонтали до 16 бит
                LD A, (IX + FObject.Position.X.High)
                LD C, A
                LD B, #00

                ; умножить координату гекса по горизонтали на HexStrideX
                ;   HexStrideX = HEXTILE_SIZE_X << 5 = 192 четверти пикселя
                LD H, B
                LD L, C
                ADD HL, HL  ; x2
                ADD HL, BC  ; x3
                ADD HL, HL  ; x6
                ADD HL, HL  ; x12
                ADD HL, HL  ; x24
                ADD HL, HL  ; x48
                ADD HL, HL  ; x96
                ADD HL, HL  ; x192

                ; добавить локальное положение объекта внутри гекса
                LD C, (IX + FObject.Position.X.Low)
                ADD HL, BC  ; HL - HexX * 192 + LocalX

                ; сформировать маску смещения строки:
                ;   -(HEXTILE_SIZE_X << 4) = -96 (#A0), если строка чётная;
                ;   #00,                              если строка нечётная
                LD A, (IX + FObject.Position.Y.High)
                RRA                                                             ; Carry - признак нечётной строки
                CCF                                                             ; Carry - признак чётной строки
                SBC A, A                                                        ; #FF для чётной строки, #00 для нечётной
                AND -(((HEXTILE_SIZE_X << 3) >> 1) << 2)                        ; половина ширины гексагона в четвертях пикселя

                ; знаково расширить смещение строки до 16 бит
                LD C, A
                ADD A, A
                SBC A, A
                LD B, A
                ADD HL, BC  ; HL - глобальная координата CurrentX
                PUSH HL                                                         ; сохранить CurrentX

                ; -----------------------------------------
                ; преобразование центра целевого гекса
                ; в глобальную координату, выраженную в четвертях пикселя:
                ;   TargetX = PathX * HexStrideX + CenterX - (1 - (PathY & 1)) * HalfHexX
                ; -----------------------------------------
                ; расширить координату целевого гекса по горизонтали до 16 бит
                LD A, (DE)                                                      ; FPath.HexCoord.X
                LD C, A
                LD B, #00

                ; умножить координату целевого гекса по горизонтали на HexStrideX
                ;   HexStrideX = HEXTILE_SIZE_X << 5 = 192 четверти пикселя
                LD H, B
                LD L, C
                ADD HL, HL  ; x2
                ADD HL, BC  ; x3
                ADD HL, HL  ; x6
                ADD HL, HL  ; x12
                ADD HL, HL  ; x24
                ADD HL, HL  ; x48
                ADD HL, HL  ; x96
                ADD HL, HL  ; x192

                ; добавить горизонтальную координату центра целевого гекса
                LD C, HEXTILE_SIZE_X << 4                                       ; центр гекса в четвертях пикселя
                ADD HL, BC  ; HL - PathX * 192 + CenterX

                ; перейти к координате Y целевого гекса без изменения старшего байта адреса
                INC E

                ; сформировать маску смещения целевой строки:
                ;   -(HEXTILE_SIZE_X << 4) = -96 (#A0), если строка чётная;
                ;   #00,                              если строка нечётная
                LD A, (DE)                                                      ; FPath.HexCoord.Y
                RRA                                                             ; Carry - признак нечётной строки
                CCF                                                             ; Carry - признак чётной строки
                SBC A, A                                                        ; #FF для чётной строки, #00 для нечётной
                AND -(((HEXTILE_SIZE_X << 3) >> 1) << 2)                        ; половина ширины гексагона в четвертях пикселя

                ; знаково расширить смещение целевой строки до 16 бит
                LD C, A
                ADD A, A
                SBC A, A
                LD B, A
                ADD HL, BC  ; HL - глобальная координата TargetX

                ; рассчитать знаковое горизонтальное смещение до точки пути
                POP BC                                                          ; BC - глобальная координата CurrentX
                OR A                                                            ; сброс Carry перед вычитанием
                SBC HL, BC                                                      ; HL - TargetX - CurrentX
                PUSH HL                                                         ; сохранить горизонтальное смещение

                ; -----------------------------------------
                ; преобразование текущей вертикальной позиции объекта
                ; в глобальную координату, выраженную в четвертях пикселя:
                ;   CurrentY = HexY * 128 + LocalY
                ;   128 - вертикальный шаг строки гексов в четвертях пикселя
                ; -----------------------------------------
                ; расширить координату строки текущего гекса до 16 бит
                LD A, (IX + FObject.Position.Y.High)
                LD L, A
                LD H, #00
                LD B, H                                                         ; B - ноль для сложения 8-битного LocalY через BC

                ; умножить координату строки текущего гекса на 128
                ADD HL, HL  ; x2
                ADD HL, HL  ; x4
                ADD HL, HL  ; x8
                ADD HL, HL  ; x16
                ADD HL, HL  ; x32
                ADD HL, HL  ; x64
                ADD HL, HL  ; x128

                ; добавить локальное положение объекта внутри строки гексов
                LD C, (IX + FObject.Position.Y.Low)
                ADD HL, BC  ; HL - глобальная координата CurrentY
                PUSH HL                                                         ; сохранить CurrentY

                ; -----------------------------------------
                ; преобразование центра целевого гекса
                ; в глобальную координату, выраженную в четвертях пикселя:
                ;   TargetY = PathY * 128 + CenterY
                ; -----------------------------------------
                ; расширить координату строки целевого гекса до 16 бит
                LD A, (DE)                                                      ; FPath.HexCoord.Y
                LD L, A
                LD H, #00                                                       ; B также сохраняет ноль от расчёта CurrentY

                ; умножить координату строки целевого гекса на 128
                ADD HL, HL  ; x2
                ADD HL, HL  ; x4
                ADD HL, HL  ; x8
                ADD HL, HL  ; x16
                ADD HL, HL  ; x32
                ADD HL, HL  ; x64
                ADD HL, HL  ; x128

                ; добавить вертикальную координату центра целевого гекса
                LD C, HEXTILE_SIZE_Y << 4                                       ; центр гекса в четвертях пикселя
                ADD HL, BC  ; HL - глобальная координата TargetY

                ; рассчитать знаковое вертикальное смещение до точки пути
                POP BC                                                          ; BC - глобальная координата CurrentY
                OR A                                                            ; сброс Carry перед вычитанием
                SBC HL, BC                                                      ; HL - TargetY - CurrentY

                ; восстановить горизонтальное смещение в выходной регистр DE
                POP DE

                POP BC                                                          ; восстановить исходное значение BC
                RET                                                             ; вернуть вертикальное смещение в HL, горизонтальное в DE

                endif ; ~_CHARACTER_PATH_DISTANCE_
